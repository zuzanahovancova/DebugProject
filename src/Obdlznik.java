public class Obdlznik {
    public static double obsah(double stranaA, double stranaB) {
        double vysledok = stranaA * stranaB;
        return vysledok;
    }
    public static double obvod(double stranaA, double stranaB) {
        double vysledok = 2 * (stranaA + stranaB);
        return vysledok;
    }
}
